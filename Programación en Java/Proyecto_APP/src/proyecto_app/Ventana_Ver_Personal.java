    private void Cargar_Empleados() {
     int j,i,Numero_de_Areas=Personal.getSize();
      for (j=0;j<Numero_de_Areas;j++){
       int Numero_de_Empleados=Personal.getItem(j).getSize();
       for (i=0;i<Numero_de_Empleados;i++){
         Persona_Seleccionada=new Clase_Persona();
         //Identificador_de_Usuario_Seleccionado=new Clase_Identificador_de_Usuario(j,i);
         Persona_Seleccionada=Personal.getItem(j).getItem(i);
         //Identificadores_de_Usuarios.addItem(Identificador_de_Usuario_Seleccionado);
         Agregar_Columna();
       }
      }
    }
    private void Agregar_Columna() {
     String Datos[]=new String[8];
     Datos[0]=Persona_Seleccionada.getNombre()+" "+Persona_Seleccionada.getApellido_Paterno()+" "+Persona_Seleccionada.getApellido_Materno();
     Datos[1]=Persona_Seleccionada.getArea();
     Datos[2]=Persona_Seleccionada.get_Tipo_de_Empleado();
     Datos[3]=Persona_Seleccionada.getUsuario();
     Datos[4]=""+Persona_Seleccionada.getNumero_Usuario();
     Datos[5]=""+Persona_Seleccionada.getAntiguedad();
     Datos[6]=""+Persona_Seleccionada.getEdad();
     Datos[7]=""+Persona_Seleccionada.getSalario();
     Tabla.addRow(Datos);
    }
