    private void Cargar_Empleados() {
     Identificadores_de_Usuarios=new Clase_Identificadores_de_Usuarios();
     switch (Persona_Editor.getTipo_de_Empleado()) {
            case 0:
                Cargar_Empleados_Gerente();
                break;
            case 1:
                Cargar_Empleados_Recursos_Humanos();
                break;
            default:
                Cargar_Empleados_Jefe();
                break;
        }
     int i;
     for (i=0;i<Identificadores_de_Usuarios.getSize();i++){
       Actualizar_Persona_Seleccionada(i);
       Agregar_Columna();
     }
      Persona_Seleccionada=new Clase_Persona();
      Identificador_de_Usuario_Seleccionado=new Clase_Identificador_de_Usuario();
      Identificador_de_Usuario_Seleccionado=Identificadores_de_Usuarios.getItem(0);
      Persona_Seleccionada=Personal.getItem(Identificador_de_Usuario_Seleccionado.Numero_de_Area).getItem(Identificador_de_Usuario_Seleccionado.Numero_de_Empleado);
    }
    private void Cargar_Empleados_Gerente() {
      int j,i,Numero_de_Areas=Personal.getSize();
      for (j=1;j<Numero_de_Areas;j++){
       int Numero_de_Empleados=Personal.getItem(j).getSize();
       for (i=0;i<Numero_de_Empleados;i++){
         Persona_Seleccionada=new Clase_Persona();
         Identificador_de_Usuario_Seleccionado=new Clase_Identificador_de_Usuario(j,i);
         Persona_Seleccionada=Personal.getItem(j).getItem(i);
         comboEmpleados.addItem(Persona_Seleccionada.getNombre()+" "+Persona_Seleccionada.getApellido_Paterno()+" "+Persona_Seleccionada.getApellido_Materno()+" ("+Persona_Seleccionada.getUsuario()+")");
         //comboEmpleados.addItem("("+Persona_Seleccionada.getUsuario()+")");
         Identificadores_de_Usuarios.addItem(Identificador_de_Usuario_Seleccionado);
       }
      }
    }
