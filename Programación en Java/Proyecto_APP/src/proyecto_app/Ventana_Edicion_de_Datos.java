      //Object Objeto=new Object({});
      
      int i,a=comboEmpleados.getSelectedIndex();
      int b=Identificadores_de_Usuarios.getSize();
      comboEmpleados.removeAllItems();
      while(Tabla.getRowCount()>0){
         Tabla.removeRow(0);
      }
