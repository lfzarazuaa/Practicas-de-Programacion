    public boolean isnan(String Texto){
        //return (float)Float.valueOf(txtCantidad.getText());
       try{
        Double.valueOf(Texto);
        return true;
       }
       catch(NumberFormatException e){
        return false;
       }
       
    }
    private void Modificar_Valor(){
     if (Inicio_Ventana){
        int a,b;
         a=comboAbr1.getSelectedIndex();
         b=comboAbr2.getSelectedIndex();
         lblNombre1.setText(Magnitud_Fisica.getItem(a).Nombre);
         lblNombre2.setText(Magnitud_Fisica.getItem(b).Nombre);
         if (isnan(txtValor1.getText())){
          Magnitud_Fisica.getItem(a).Valor=Double.valueOf(txtValor1.getText());
          Magnitud_Fisica.Realizar_Conversion(Magnitud_Fisica.getItem(a),Magnitud_Fisica.getItem(b));
          txtValor2.setText(""+Magnitud_Fisica.getItem(b).Valor);
          Modificar_Tabla();
         }
         else
         {
           txtValor1.setText("1");
           Magnitud_Fisica.getItem(a).Valor=Double.valueOf(txtValor1.getText());
           Magnitud_Fisica.Realizar_Conversion(Magnitud_Fisica.getItem(a),Magnitud_Fisica.getItem(b));
           txtValor2.setText(""+Magnitud_Fisica.getItem(b).Valor);
           JOptionPane.showMessageDialog(null, "Ingrese un valor válido",
           "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);   
         }
        }
    }
    public void Cargar_Menu(Clase_Magnitud_Fisica Magnitud_Fisica) {
        this.Magnitud_Fisica=Magnitud_Fisica;
        int tamano=Magnitud_Fisica.getSize();
        comboAbr1.removeAllItems();
        comboAbr2.removeAllItems();
        lblNombre1.setText(Magnitud_Fisica.getItem(0).Nombre);
        lblNombre2.setText(Magnitud_Fisica.getItem(1).Nombre);
        txtValor1.setText(""+Magnitud_Fisica.getItem(0).Valor);
        Magnitud_Fisica.Realizar_Conversion(Magnitud_Fisica.getItem(0),Magnitud_Fisica.getItem(1));
        txtValor2.setText(""+Magnitud_Fisica.getItem(1).Valor);
        for (int i=0;i<tamano;i++){
         comboAbr1.addItem(Magnitud_Fisica.getItem(i).Abreviatura);
         comboAbr2.addItem(Magnitud_Fisica.getItem(i).Abreviatura);
        }
        comboAbr2.setSelectedIndex(1);
        Inicio_Ventana=true;
        Modificar_Tabla();
    }
