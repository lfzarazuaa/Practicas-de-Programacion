    public void Modificar_Tabla(){
        int a;
        a=comboAbr1.getSelectedIndex();
        Magnitud_Fisica.getItem(a).Valor=Double.valueOf(txtValor1.getText());
        while(Tabla.getRowCount()>0){
         Tabla.removeRow(0);
        }
        for (int b=0;b<Magnitud_Fisica.getSize();b++){
         String Datos[]=new String[3];
         Magnitud_Fisica.Realizar_Conversion(Magnitud_Fisica.getItem(a),Magnitud_Fisica.getItem(b));
         Datos[0]=Magnitud_Fisica.getItem(b).Nombre;
         Datos[1]=""+Magnitud_Fisica.getItem(b).Valor;
         Datos[2]=""+Magnitud_Fisica.getItem(b).Abreviatura;
         Tabla.addRow(Datos);
        }
    }
