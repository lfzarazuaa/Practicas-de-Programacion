    private void Edicion_de_Empleados(){
     Clase_Persona Persona_Seleccionada;
     int area=Persona.getNumero_Area();
     int i,Numero_de_Empleados=Personal.getItem(area).getSize();
     int Trabajadores=0;
     for (i=0;i<Numero_de_Empleados;i++){
         Persona_Seleccionada=new Clase_Persona();
         Persona_Seleccionada=Personal.getItem(area).getItem(i);
         if(Persona_Seleccionada.getTipo_de_Empleado()!=2){
          Trabajadores=Trabajadores+1;
         }
       }
     if (Trabajadores>0){
     Edicion_de_Datos=new Ventana_Edicion_de_Datos();
     Edicion_de_Datos.Modificar_Informacion_Empleados(Identificador_de_Usuario);
     Edicion_de_Datos.setVisible(true);
     this.setVisible(false);}
     else{
       JOptionPane.showMessageDialog(null, "No se puede editar ya que no posee trabajadores a administrar",
                "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);  
       //System.out.println("No posee trabajadores a administrar");
     }
    }

