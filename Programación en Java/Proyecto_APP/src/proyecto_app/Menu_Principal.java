    public void Cargar_datos_de_Usuario(Clase_Identificador_de_Usuario Identificador_de_Usuario) {
     setLocationRelativeTo(this);
     Persona=new Clase_Persona();
     this.Identificador_de_Usuario.Numero_de_Area=Identificador_de_Usuario.Numero_de_Area;
     this.Identificador_de_Usuario.Numero_de_Empleado=Identificador_de_Usuario.Numero_de_Empleado;
     Persona=Personal.getItem(Identificador_de_Usuario.Numero_de_Area).getItem(Identificador_de_Usuario.Numero_de_Empleado);
     txtNombre.setText(Persona.getNombre()+" "+Persona.getApellido_Paterno()+" "+Persona.getApellido_Materno());
     txtArea.setText(Persona.getArea());
     txtUsuario.setText(Persona.getUsuario());
     txtSalario.setText(""+Persona.getSalario());
     txtAntiguedad.setText(""+Persona.Antiguedad+" a√±os");
     txtEdad.setText(""+Persona.Edad+" a√±os");
     txtNumerodeUsuario.setText(""+Persona.getNumero_Usuario());
     txtTipodeEmpleado.setText(Persona.get_Tipo_de_Empleado());
     comboOpciones.removeAllItems();
     int i;
     String[] Cadena=Persona.Obtener_Opciones();
     for (i=0;i<Cadena.length;i++){
     comboOpciones.addItem(Cadena[i]);}
    }

