var numero = 10;

switch (numero) {
    case 1:
        console.log("¡Soy un uno!")
        break;
    case 10:
        console.log("¡Soy un diez!");
        break;
    case 100:
        console.log("¡Soy un cien!");
        break;
    case 1000:
        console.log("¡Soy un mil!");
        break;
    default:
        console.log("¡Soy otro número!");
        break;
}